#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { execFileSync } from "node:child_process";
import { fileURLToPath } from "node:url";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const MANIFEST_PATH = path.join(ROOT, "wire/error-codes.json");
const CHECK_MODE = process.argv.includes("--check");

const manifest = JSON.parse(fs.readFileSync(MANIFEST_PATH, "utf8"));
validateManifest(manifest);
const codes = manifest.codes;

const outputs = new Map([
  ["crates/obu-wire/src/error_codes.generated.rs", formatRust(generateRust(codes))],
  ["packages/sdk/src/error_codes.generated.ts", generateTs(codes)],
]);

let stale = false;
for (const [relativePath, content] of outputs) {
  const absolutePath = path.join(ROOT, relativePath);
  if (CHECK_MODE) {
    const current = fs.existsSync(absolutePath) ? fs.readFileSync(absolutePath, "utf8") : "";
    if (current !== content) {
      stale = true;
      console.error(`${relativePath} is stale; run node scripts/generate-error-codes.mjs`);
    }
  } else {
    fs.mkdirSync(path.dirname(absolutePath), { recursive: true });
    fs.writeFileSync(absolutePath, content);
  }
}

if (stale) process.exit(1);

function validateManifest(manifest) {
  if (manifest.schemaVersion !== 1 || !Array.isArray(manifest.codes) || manifest.codes.length === 0) {
    throw new Error("wire/error-codes.json must contain schemaVersion 1 and a non-empty codes array");
  }
  const constants = new Set();
  const numbers = new Set();
  for (const entry of manifest.codes) {
    if (typeof entry.constant !== "string" || !/^ERR_[A-Z0-9_]*$/.test(entry.constant)) {
      throw new Error(`invalid error constant ${JSON.stringify(entry.constant)}`);
    }
    if (!Number.isInteger(entry.code) || entry.code >= 0) {
      throw new Error(`error code for ${entry.constant} must be a negative integer`);
    }
    if (
      !Array.isArray(entry.doc) ||
      entry.doc.length === 0 ||
      !entry.doc.every((line) => typeof line === "string")
    ) {
      throw new Error(`error ${entry.constant} must have a non-empty doc string array`);
    }
    if (constants.has(entry.constant)) throw new Error(`duplicate constant ${entry.constant}`);
    if (numbers.has(entry.code)) throw new Error(`duplicate code ${entry.code}`);
    constants.add(entry.constant);
    numbers.add(entry.code);
  }
}

function generateRust(codes) {
  const lines = [
    "//! JSON-RPC wire error code constants.",
    "//!",
    "//! Generated by `node scripts/generate-error-codes.mjs` from `wire/error-codes.json`.",
    "//! Do not edit by hand.",
    "",
  ];
  for (const entry of codes) {
    for (const docLine of entry.doc) {
      lines.push(docLine.length > 0 ? `/// ${docLine}` : "///");
    }
    lines.push(`pub const ${entry.constant}: i32 = ${entry.code};`);
  }
  lines.push("");
  return lines.join("\n");
}

function generateTs(codes) {
  const lines = [
    "// Generated from wire/error-codes.json by scripts/generate-error-codes.mjs.",
    "// Do not edit by hand.",
    "",
  ];
  for (const entry of codes) {
    if (entry.doc.length === 1) {
      lines.push(`/** ${entry.doc[0]} */`);
    } else {
      lines.push("/**");
      for (const docLine of entry.doc) lines.push(docLine.length > 0 ? ` * ${docLine}` : " *");
      lines.push(" */");
    }
    lines.push(`export const ${entry.constant} = ${entry.code};`);
  }
  lines.push("");
  return lines.join("\n");
}

function formatRust(source) {
  try {
    return execFileSync("rustfmt", ["--emit", "stdout", "--edition", "2024"], {
      input: source,
      encoding: "utf8",
      maxBuffer: 1024 * 1024,
    });
  } catch (error) {
    const detail = error.stderr || error.message || String(error);
    throw new Error(`rustfmt failed while generating error code file: ${detail}`);
  }
}
