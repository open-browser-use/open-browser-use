#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { execFileSync } from "node:child_process";
import { fileURLToPath } from "node:url";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const MANIFEST_PATH = path.join(ROOT, "wire/methods.json");
const CHECK_MODE = process.argv.includes("--check");

const POLICIES = new Set([
  "always-allowed",
  "target-url",
  "current-origin",
  "history",
  "download",
  "upload",
  "raw-cdp",
  "internal-lifecycle",
]);
const SUPPORT_STATES = new Set([
  "implemented",
  "unsupported",
  "intentionally-not-implemented",
]);
const RUST_POLICY = {
  "always-allowed": "AlwaysAllowed",
  "target-url": "TargetUrl",
  "current-origin": "CurrentOrigin",
  history: "History",
  download: "Download",
  upload: "Upload",
  "raw-cdp": "RawCdp",
  "internal-lifecycle": "InternalLifecycle",
};
const RUST_SUPPORT = {
  implemented: "Implemented",
  unsupported: "Unsupported",
  "intentionally-not-implemented": "IntentionallyNotImplemented",
};

const manifest = readManifest();
const methods = manifest.methods;
validateManifest(methods);

const outputs = new Map([
  ["packages/sdk/src/wire/methods.ts", generateTsMethods(methods)],
  ["packages/sdk/src/wire/method-policy.ts", generateTsMethodPolicy(methods)],
  ["crates/obu-host/src/methods.rs", formatRust(generateRustMethods(methods))],
  ["crates/obu-host/src/generated/method_policy.rs", formatRust(generateRustMethodPolicy(methods))],
]);

let stale = false;
for (const [relativePath, content] of outputs) {
  const absolutePath = path.join(ROOT, relativePath);
  if (CHECK_MODE) {
    const current = fs.existsSync(absolutePath) ? fs.readFileSync(absolutePath, "utf8") : "";
    if (current !== content) {
      stale = true;
      console.error(`${relativePath} is stale; run node scripts/generate-wire-methods.mjs`);
    }
  } else {
    fs.mkdirSync(path.dirname(absolutePath), { recursive: true });
    fs.writeFileSync(absolutePath, content);
  }
}

if (stale) process.exit(1);

function readManifest() {
  return JSON.parse(fs.readFileSync(MANIFEST_PATH, "utf8"));
}

function validateManifest(methods) {
  if (!Array.isArray(methods) || methods.length === 0) {
    throw new Error("wire/methods.json must contain a non-empty methods array");
  }
  const constants = new Set();
  const names = new Set();
  for (const method of methods) {
    requireString(method.constant, "constant");
    requireString(method.name, "name");
    requireString(method.policy, "policy");
    requireString(method.family, "family");
    if (!/^[A-Z][A-Z0-9_]*$/.test(method.constant)) {
      throw new Error(`invalid method constant ${method.constant}`);
    }
    if (constants.has(method.constant)) throw new Error(`duplicate constant ${method.constant}`);
    if (names.has(method.name)) throw new Error(`duplicate method name ${method.name}`);
    constants.add(method.constant);
    names.add(method.name);
    if (!POLICIES.has(method.policy)) {
      throw new Error(`invalid policy ${method.policy} for ${method.constant}`);
    }
    if (!method.support || typeof method.support !== "object") {
      throw new Error(`missing support map for ${method.constant}`);
    }
    for (const backend of ["cdp", "webextension"]) {
      const state = method.support[backend];
      if (!SUPPORT_STATES.has(state)) {
        throw new Error(`invalid ${backend} support ${state} for ${method.constant}`);
      }
    }
  }
}

function requireString(value, field) {
  if (typeof value !== "string" || value.length === 0) {
    throw new Error(`method entry has invalid ${field}`);
  }
}

function generatedHeader(sourceComment) {
  return [
    sourceComment,
    " *",
    " * Generated by `node scripts/generate-wire-methods.mjs`.",
    " * Do not edit by hand.",
    " */",
    "",
  ].join("\n");
}

function generateTsMethods(methods) {
  const lines = [
    generatedHeader("/**\n * Names of every JSON-RPC method on the obu-host wire."),
  ];
  let currentFamily = "";
  for (const method of methods) {
    if (method.family !== currentFamily) {
      currentFamily = method.family;
      lines.push(`// ${currentFamily}`);
    }
    lines.push(`export const ${method.constant} = ${JSON.stringify(method.name)} as const;`);
  }
  lines.push("");
  lines.push("/** Every inbound (SDK -> obu-host) method name. */");
  lines.push("export const ALL_INBOUND_METHODS = [");
  for (const method of methods) lines.push(`  ${method.constant},`);
  lines.push("] as const satisfies readonly string[];");
  lines.push("");
  return lines.join("\n");
}

function generateTsMethodPolicy(methods) {
  const lines = [
    generatedHeader("/**\n * Policy classification for every obu-host wire method."),
    'import type { MethodClassification } from "../guards.js";',
    'import * as M from "./methods.js";',
    "",
    "export const METHOD_CLASSIFICATION: Readonly<Record<string, MethodClassification>> = Object.freeze({",
  ];
  for (const method of methods) lines.push(`  [M.${method.constant}]: ${JSON.stringify(method.policy)},`);
  lines.push("});");
  lines.push("");
  return lines.join("\n");
}

function generateRustMethods(methods) {
  const lines = [
    "//! Names of every JSON-RPC method routed by `obu-host`.",
    "//!",
    "//! Generated by `node scripts/generate-wire-methods.mjs` from `wire/methods.json`.",
    "//! Do not edit by hand.",
    "",
    "#![allow(missing_docs)]",
    "",
  ];
  let currentFamily = "";
  for (const method of methods) {
    if (method.family !== currentFamily) {
      currentFamily = method.family;
      lines.push(`// ${currentFamily}`);
    }
    lines.push(`pub const ${method.constant}: &str = ${JSON.stringify(method.name)};`);
  }
  lines.push("");
  lines.push("/// All inbound (SDK -> obu-host) method names.");
  lines.push("pub const ALL_INBOUND_METHODS: &[&str] = &[");
  for (const method of methods) lines.push(`    ${method.constant},`);
  lines.push("];");
  lines.push("");
  lines.push("/// Backend support state for a wire method.");
  lines.push("#[derive(Debug, Clone, Copy, PartialEq, Eq)]");
  lines.push("pub enum BackendMethodSupport {");
  lines.push("    Implemented,");
  lines.push("    Unsupported,");
  lines.push("    IntentionallyNotImplemented,");
  lines.push("}");
  lines.push("");
  lines.push("/// Backend support matrix generated from the wire method manifest.");
  lines.push("pub const BACKEND_METHOD_SUPPORT: &[(&str, BackendMethodSupport, BackendMethodSupport)] = &[");
  for (const method of methods) {
    lines.push(
      `    (${method.constant}, BackendMethodSupport::${RUST_SUPPORT[method.support.cdp]}, BackendMethodSupport::${RUST_SUPPORT[method.support.webextension]}),`,
    );
  }
  lines.push("];");
  lines.push("");
  appendUnsupportedList(lines, "CDP_UNSUPPORTED_METHODS", methods, "cdp");
  appendUnsupportedList(lines, "WEBEXTENSION_UNSUPPORTED_METHODS", methods, "webextension");
  appendStateList(lines, "CDP_INTENTIONALLY_NOT_IMPLEMENTED_METHODS", methods, "cdp", "intentionally-not-implemented");
  appendStateList(
    lines,
    "WEBEXTENSION_INTENTIONALLY_NOT_IMPLEMENTED_METHODS",
    methods,
    "webextension",
    "intentionally-not-implemented",
  );
  return lines.join("\n");
}

function appendUnsupportedList(lines, name, methods, backend) {
  lines.push(`/// Methods advertised as unsupported by the ${backend} backend.`);
  lines.push(`pub const ${name}: &[&str] = &[`);
  for (const method of methods) {
    if (method.support[backend] === "unsupported") lines.push(`    ${method.constant},`);
  }
  lines.push("];");
  lines.push("");
}

function appendStateList(lines, name, methods, backend, state) {
  lines.push(`/// Methods marked ${state} for the ${backend} backend.`);
  lines.push(`pub const ${name}: &[&str] = &[`);
  for (const method of methods) {
    if (method.support[backend] === state) lines.push(`    ${method.constant},`);
  }
  lines.push("];");
  lines.push("");
}

function generateRustMethodPolicy(methods) {
  const lines = [
    "// Policy classification for every obu-host wire method.",
    "//",
    "// Generated by `node scripts/generate-wire-methods.mjs` from `wire/methods.json`.",
    "// Do not edit by hand.",
    "",
    "/// Explicit policy matrix for every supported inbound browser method.",
    "pub const METHOD_POLICY_CLASSIFICATIONS: &[(&str, MethodPolicyKind)] = &[",
  ];
  for (const method of methods) {
    lines.push(`    (methods::${method.constant}, MethodPolicyKind::${RUST_POLICY[method.policy]}),`);
  }
  lines.push("];");
  lines.push("");
  return lines.join("\n");
}

function formatRust(source) {
  try {
    return execFileSync("rustfmt", ["--emit", "stdout", "--edition", "2024"], {
      input: source,
      encoding: "utf8",
      maxBuffer: 1024 * 1024,
    });
  } catch (error) {
    const detail = error.stderr || error.message || String(error);
    throw new Error(`rustfmt failed while generating Rust wire method files: ${detail}`);
  }
}
